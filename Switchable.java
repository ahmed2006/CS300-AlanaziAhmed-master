
/**
 * Switchable interface.
 *
 * @author ahmed
 * @version 2018.04.02
 */
public interface Switchable {

    /**
     * switch on.
     */
    public void switchOn();

    /**
     * switch off.
     */
    public void switchOff();

    /**
     * is on or off?.
     * @return boolean
     */
    public boolean isOn();
}
