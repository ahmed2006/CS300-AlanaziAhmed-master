
/**
 * Equippable interface.
 *
 * @author ahmed
 * @version 2018.04.02
 */
public interface Equippable {

    /**
     * equip.
     */
    public void equip();

    /**
     * unequip.
     */
    public void unequip();

    /**
     * is equipped or not.
     *
     * @return isEquiped
     */
    public boolean isEquiped();
}
